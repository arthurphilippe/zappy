	#define ANSI_BOLD_COLOR_GREEN		"\x1b[1;32m"
	#define ANSI_BOLD_COLOR_YELLOW		"\x1b[1;33m"
	#define ANSI_BOLD_COLOR_BLUE		"\x1b[1;34m"
	#define ANSI_BOLD_COLOR_MAGENTA		"\x1b[1;35m"
	#define ANSI_BOLD_COLOR_CYAN		"\x1b[1;36m"
	#define ANSI_BOLD_COLOR_RESET		"\x1b[1;0m"

#endif /* !COLOR_H_ */
